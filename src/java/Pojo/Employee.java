package Pojo;

/**
 *
 * @author Dilip J Sarvaiya
 */
public class Employee {
    
    String name;
    String email;
    String salary;
    int age,id;
    String gender;
    String department;

    
     public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSalary() {
        return salary;
    }

    public void setSalary(String salary) {
        this.salary = salary;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public Employee(int id,String name, String email, String salary, int age, String gender, String department) {
        this.id=id;
        this.name = name;
        this.email = email;
        this.salary = salary;
        this.age = age;
        this.gender = gender;
        this.department = department;
    }
    
}
